def make_example(scenario: dict[str, object], variant: int) -> dict[str, object]:
    order_id = f"TEST-{SCENARIOS.index(scenario) + 1:02d}-{variant + 1:03d}"
    item = scenario["items"][variant % len(scenario["items"])]
    days = 2 + variant % 8
    query = scenario["queries"][variant % len(scenario["queries"])].format(
        item=item, days=days, order_id=order_id
    )
    if variant % 4 == 1:
        query = f"麻烦尽快处理：{query}"
    elif variant % 4 == 2:
        query = f"你好，想咨询一下，{query}"
    elif variant % 4 == 3:
        query = f"请给我具体操作步骤。{query}"

    context = CONTEXTS[variant % len(CONTEXTS)].format(order_id=order_id)
    reply = scenario["reply"].format(item=item, days=days, order_id=order_id)
    example: dict[str, object] = {
        "system": SYSTEM_PROMPT,
        "instruction": query,
        "input": context,
        "output": reply,
    }
    if variant % 5 == 4:
        example["history"] = [
            ["你好，我有一个订单问题。", "您好，请告诉我具体问题，并注意不要发送密码或验证码。"],
            [f"订单号是 {order_id}。", "收到，我会基于订单页面信息为您说明处理步骤。"],
        ]
    return example


def write_json(path: Path, records: list[dict[str, object]]) -> None:
    path.write_text(json.dumps(records, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    DATASET_DIR.mkdir(parents=True, exist_ok=True)
    train: list[dict[str, object]] = []
    validation: list[dict[str, object]] = []
    smoke: list[dict[str, object]] = []

    for scenario in SCENARIOS:
        records = [make_example(scenario, variant) for variant in range(26)]
        train.extend(records[:20])
        validation.extend(records[20:24])
        smoke.extend(records[24:26])

    write_json(DATASET_DIR / "customer_service_train.json", train)
    write_json(DATASET_DIR / "customer_service_validation.json", validation)
    write_json(DATASET_DIR / "customer_service_smoke.json", smoke)

    print(f"train={len(train)} validation={len(validation)} smoke={len(smoke)}")


if __name__ == "__main__":
    main()
